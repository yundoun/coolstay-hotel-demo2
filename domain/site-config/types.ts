/**
 * 호텔 웹사이트 템플릿 설정 타입
 * ─────────────────────────────
 * 새 호텔 세팅 시 hotel-data/ 폴더에서 이 타입에 맞춰 값을 채우면 됩니다.
 * 실시간 객실 가격·예약은 API에서 자동으로 가져옵니다.
 */

export type SiteConfig = {
  /* ══════════════════════════════════════
     기본 정보
     ══════════════════════════════════════ */
  /** 호텔 고유 ID (폴더명과 동일하게) */
  id: string;
  /** 호텔 정식 명칭 (예: "경주 황리단길 팰리스 호텔") */
  name: string;
  /** 소재 도시 (예: "경주") */
  city: string;
  /** 등급 (4성 or 5성) */
  grade: 4 | 5;
  /** 주소 — 지도 검색에도 사용됨 (예: "경북 경주시 봉황로51번길 11") */
  address: string;
  /** 대표 연락처 (예: "010-2881-4995") */
  phone: string;
  /** 체크인 시간 (예: "15:00") */
  checkInTime: string;
  /** 체크아웃 시간 (예: "11:00") */
  checkOutTime: string;

  /* ══════════════════════════════════════
     Hero 섹션
     ══════════════════════════════════════ */
  /** Hero 배너 슬라이드 이미지 URL (최대 5장, 10초 간격 자동 전환) */
  heroImages: string[];
  /** 한 줄 컨셉 문구 — 브라우저 탭 제목에 표시 (예: "황리단길 도보 6분, 경주의 중심에서 만나는 프리미엄 호텔") */
  shortConcept: string;
  /** 호텔명이 길면 "sm"으로 설정 — 히어로 제목 크기 조절 (기본 "base") */
  heroTitleSize?: "base" | "sm";

  /* ══════════════════════════════════════
     인사말 섹션
     ══════════════════════════════════════ */
  greeting: {
    /** 인사말 제목 (줄바꿈: \n) */
    headline: string;
    /** 인사말 본문 (줄바꿈: \n) */
    body: string;
    /** 서명 (예: "OO호텔 일동") */
    signature: string;
  };

  /* ══════════════════════════════════════
     호텔 소개(About) 섹션
     ══════════════════════════════════════ */
  about: {
    /** 섹션 소제목 — 제목 위에 작게 표시 (예: "Story") */
    subtitle?: string;
    /** 제목 (줄바꿈: \n) */
    title: string;
    /** 본문 설명 */
    body?: string;
    /** 갤러리 이미지 URL 목록 (최대 5장) */
    images: string[];
  };

  /* ══════════════════════════════════════
     찾아오는 길 섹션
     ══════════════════════════════════════ */
  directions: {
    /** 주차 안내 — 없으면 빈 문자열 (화면에 표시되지 않음) */
    parkingInfo: string;
    /** 주변 관광지·교통 안내 (예: { label: "경주 중앙시장", value: "도보 2분" }) */
    nearbyItems: { label: string; value: string }[];
  };
};
